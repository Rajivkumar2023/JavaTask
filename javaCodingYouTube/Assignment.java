                    /*Asssigment Operator Example */

public class Assignment {
    public static void main(String[] args){
        int a;
        a = 10; ///simple
        System.out.println(a);//output:- 10

        a+=10;  ///compound assignment operator (a=a+10)
        System.out.println(a); ///output:- 20


        a-=10; ///(a=a-10)
        System.out.println(a); //output:- 10
    }
}
